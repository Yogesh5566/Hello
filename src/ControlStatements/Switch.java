package ControlStatements;

public class Switch {
public static void main(String[] args) {
	switch("yogesh") {
	case "dhumal":{
		System.out.println("Dangerous");
		break;
	}
	case "yogesh":{
		System.out.println("Hacker");
		break;
	}
	case "Khushal":{
		System.out.println("Computer Engineer");
		break;
	}
	}
}
}
